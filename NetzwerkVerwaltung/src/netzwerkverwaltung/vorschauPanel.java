    public void setText(ArrayList<String> getText, String versender, String empfaenger, String Betreff){
       String setTee = "",setAnhaenge = "<br><br><br><br><hr><br> Anhaenge: <br><br>";
       sendDateien = getText;
       String info = "<table border=0 width=100% style=background-color:#cdd5df align=center> "
               + "<tr><td width=25 >Versender: </td><td>"+versender+"</td>"
               + "<tr><td width=25>Empf√§nger: </td><td>"+empfaenger+"</td>"
               + "<tr><td width=25>Betreff: </td><td>"+Betreff +"</td>"
               + "<tr><td width=25>Datum: </td><td>"+new SimpleDateFormat("dd.MM.yyy HH:MM").format(new Date()) +"</td>"
               + "</table>";
       for (int i=1;i<getText.size()-1;i++){
           if (getText.get(i).split(client.trennzeichen)[1].equals("ANHAENGE")){
             setAnhaenge += getText.get(i).split(client.trennzeichen)[2].split("\\\\")[getText.get(i).split(client.trennzeichen)[2].split("\\\\").length-1];
           }else
             setTee += getText.get(i).split(client.trennzeichen)[1];
       }
       setTee += setAnhaenge;
       betreff.setText(info);
       vorschauInhalt.setText(setTee);
       this.setVisible(true);
   }
