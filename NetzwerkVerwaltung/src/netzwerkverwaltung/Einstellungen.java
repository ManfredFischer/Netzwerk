    private String checkDaten(String Check) {
        if (Check.equals("")) {
            Check = "---";
        }
        String tm="";
        String[] temp = Check.split("");
        for (int i=0;i<temp.length;i++){
            if (temp[i].equals("\\+")){
              tm = tm + "-?-";  
            }else{
              tm = tm + temp[i]; 
            }
        }
        return tm;
    }
    
    /** Creates new form Einstellungen */
    public Einstellungen() {
        initComponents();
        JTable tabZustaendigkeit = new JTable(tableZustaendigkeit);
        JTable tabPfad = new JTable(tablePfad);

        ADSScrollPfad.add(tabPfad);
        ADSScrollPfad.setViewportView(tabPfad);

        zustaendigkeiten.add(tabZustaendigkeit);
        zustaendigkeiten.setViewportView(tabZustaendigkeit);
        try {
            BufferedReader read = null;
            read = new BufferedReader(new FileReader(new File("Config/netz.cfg")));
            String DatenTemp = read.readLine();
            while (DatenTemp != null) {
                if (DatenTemp.startsWith("##Server")) {
                    DatenTemp = read.readLine();
                    ServerIP.setText(DatenTemp.substring(4).split(":")[0]);
                    ServerPort.setText(DatenTemp.substring(4).split(":")[1]);
                }
                if (DatenTemp.startsWith("##Zeiten")) {
                    DatenTemp = read.readLine();
                    timeHaupt.setText(DatenTemp.substring(8));
                    DatenTemp = read.readLine();
                    timeStoerung.setText(DatenTemp.substring(8));
                }
                DatenTemp = read.readLine();
            }
            read.close();
            read = new BufferedReader(new FileReader(new File("Config/config.cfg")));
            DatenTemp = read.readLine();
            while (DatenTemp != null) {
                if (DatenTemp.startsWith("##ADS Einstellungen")) {
                    DatenTemp = read.readLine();
                    ADSServer.setText(DatenTemp.substring(13));
                    DatenTemp = read.readLine();
                    ADSUser.setText(DatenTemp.substring(15));
                    DatenTemp = read.readLine();
                    ADSPW.setText(DatenTemp.substring(13));
                }
                if (DatenTemp.startsWith("##ADS Pfad")) {
                    DatenTemp = read.readLine();
                    while(!DatenTemp.startsWith("##")){
                      Object[] temp = new Object[2];
                      temp[0]=DatenTemp.split("Pfad: ")[0].substring(10);
                      temp[1]=DatenTemp.split("Pfad: ")[1];
                      tablePfad.addRow(temp); 
                      DatenTemp = read.readLine();
                    }
                }
                if (DatenTemp.startsWith("##Zuständigkeit")) {
                    DatenTemp = read.readLine();
                    while(DatenTemp != null){
                      Object[] temp = new Object[3];
                      temp[0]=DatenTemp.split("Zustaendige: ")[0].substring(6);
                      temp[1]=DatenTemp.split("Zustaendige: ")[1].split(" eMail: ")[0];
                      temp[2]=DatenTemp.split("Zustaendige: ")[1].split(" eMail: ")[1];
                      tablePfad.addRow(temp); 
                      DatenTemp = read.readLine();
                    }
                }
                DatenTemp = read.readLine();
            }
            read.close();
        } catch (Exception ex) {
        }

        this.setVisible(true);
    }

