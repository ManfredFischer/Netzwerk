        schreib2.write("##ADS Einstellungen\n");
        schreib2.append("ADSServer IP: " + checkDaten(ADSServer.getText()) + "\n");
        schreib2.append("ADSServer User: " + checkDaten(ADSUser.getText()) + "\n");
        schreib2.append("ADSServer PW: " + checkDaten(ADSPW.getText()) + "\n");
